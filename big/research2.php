<p>
    How to create an HTML gallery?
</p>
<p>
    <em>Step 1:</em>
</p>
<p>
    In HTML, declare each image thumbnail into div containers and create a div
    container for the clicked image (full screen view).
    <br/>
    <br/>
</p>

<pre>

&lt;!-- The grid: four columns --&gt;
&lt;div class=&quot;row&quot;&gt;
  &lt;div class=&quot;column&quot;&gt;
    &lt;img src=&quot;img_nature.jpg&quot; alt=&quot;Nature&quot; onclick=&quot;myFunction(this);&quot;&gt;
  &lt;/div&gt;
  &lt;div class=&quot;column&quot;&gt;
    &lt;img src=&quot;img_snow.jpg&quot; alt=&quot;Snow&quot; onclick=&quot;myFunction(this);&quot;&gt;
  &lt;/div&gt;
  &lt;div class=&quot;column&quot;&gt;
    &lt;img src=&quot;img_mountains.jpg&quot; alt=&quot;Mountains&quot; onclick=&quot;myFunction(this);&quot;&gt;
  &lt;/div&gt;
  &lt;div class=&quot;column&quot;&gt;
    &lt;img src=&quot;img_lights.jpg&quot; alt=&quot;Lights&quot; onclick=&quot;myFunction(this);&quot;&gt;
  &lt;/div&gt;
&lt;/div&gt;

&lt;!-- The expanding image container --&gt;
&lt;div class=&quot;container&quot;&gt;
  &lt;!-- Close the image --&gt;
  &lt;span onclick=&quot;this.parentElement.style.display='none'&quot; class=&quot;closebtn&quot;&gt;&times;&lt;/span&gt;

  &lt;!-- Expanded image --&gt;
  &lt;img id=&quot;expandedImg&quot; style=&quot;width:100%&quot;&gt;

  &lt;!-- Image text --&gt;
  &lt;div id=&quot;imgtext&quot;&gt;&lt;/div&gt;
&lt;/div&gt;

</pre>



<p>
    <em>Step 2:</em>
</p>
<p>
    In CSS, Create four columns and style the images:
    <br/>
    <br/>
</p>

<pre>
/* The grid: Four equal columns that floats next to each other */
.column {
  float: left;
  width: 25%;
  padding: 10px;
}

/* Style the images inside the grid */
.column img {
  opacity: 0.8; 
  cursor: pointer; 
}

.column img:hover {
  opacity: 1;
}

/* Clear floats after the columns */
.row:after {
  content: "";
  display: table;
  clear: both;
}

/* The expanding image container (positioning is needed to position the close button and the text) */
.container {
  position: relative;
  display: none;
}

/* Expanding image text */
#imgtext {
  position: absolute;
  bottom: 15px;
  left: 15px;
  color: white;
  font-size: 20px;
}

/* Closable button inside the image */
.closebtn {
  position: absolute;
  top: 10px;
  right: 15px;
  color: white;
  font-size: 35px;
  cursor: pointer;
}

</pre>

<p>
    <em>Step 3:</em>
</p>
<p>
    Add JavaScript, 
    <br/>
    <br/>
</p>

<pre>
function myFunction(imgs) {
  // Get the expanded image
  let expandImg = document.getElementById("expandedImg");
  // Get the image text
  let imgText = document.getElementById("imgtext");
  // Use the same src in the expanded image as the image being clicked on from the grid
  expandImg.src = imgs.src;
  // Use the value of the alt attribute of the clickable image as text inside the expanded image
  imgText.innerHTML = imgs.alt;
  // Show the container element (hidden with CSS)
  expandImg.parentElement.style.display = "block";
}

</pre>


<p>
    Notice that how &lt;img id="expandedImg"&gt; and &lt;div id="imgtext"&gt;    are used among HTML, CSS, and JavaScript. These create uniformity allowing
    each language to understand the same element ids. Thus, a slideshow is
    created on the website.
</p>
